public class Variables {
    public static void main(String[] args) {

        //Variables: int
        int numberOfMemebersInFootballTeam = 11;
        int maxGoalsScoredPerMatch = 149;
        System.out.println(numberOfMemebersInFootballTeam);
        System.out.println(maxGoalsScoredPerMatch);

        //Variables: double
        double applesWeight = 2.352;
        double applesPrice = 1.59;
        System.out.println(applesWeight);
        System.out.println(applesPrice);

        //Variables: float
        float engineDisplacement = 1.954f;
        float fuelFlow = 5.36f;
        System.out.println(engineDisplacement);
        System.out.println(fuelFlow);

        //Char
        char h = 72;
        System.out.println(h);
        char i = 105;
        System.out.println(i);
        char comma = 44;
        System.out.println(comma);
        char n = 78;
        System.out.println(n);
        System.out.println(i);
        char k = 107;
        System.out.println(k);
        System.out.println(i);
        char t = 116;
        System.out.println(t);
        char a = 97;
        System.out.println(a);
        char mark = 33;
        System.out.println(mark);

        //Boolean
        boolean isMarch = true;
        boolean isNight = false;
        boolean hasMissedLastSession = false;
        System.out.println(isMarch);
        System.out.println(isNight);
        System.out.println(hasMissedLastSession);
    }
}
